#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int i, char c))
{
	unsigned int i;
	char *res;

	i = 0;
	res = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!res)
		return (NULL);
	while (i < ft_strlen(s))
	{
		res[i] = (*f)(i, s[i]);
		i++;
	}
	res[i] = 0;
	return (res);
}